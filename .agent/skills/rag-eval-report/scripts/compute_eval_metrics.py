#!/usr/bin/env python3
"""
RAG Evaluation Report Generator
================================
Reads eval_*.jsonl files from an experiment directory, computes average metrics,
and generates a comprehensive Markdown comparison report.

Usage:
    python compute_eval_metrics.py <experiment_dir> [--output_dir <output_dir>]
"""

import json
import os
import sys
import math
import glob
import argparse


def load_jsonl(filepath):
    """Load a JSONL file, returning a list of parsed JSON objects."""
    data = []
    with open(filepath, "r", encoding="utf-8") as f:
        for i, line in enumerate(f, 1):
            if line.strip():
                try:
                    data.append(json.loads(line))
                except json.JSONDecodeError as e:
                    print(f"Warning: Failed to parse line {i} in {filepath}: {e}", file=sys.stderr)
    return data


def mean(values):
    """Compute arithmetic mean."""
    return sum(values) / len(values) if values else 0.0


def stdev(values):
    """Compute population standard deviation."""
    if len(values) < 2:
        return 0.0
    m = mean(values)
    return math.sqrt(sum((x - m) ** 2 for x in values) / len(values))


METRICS_KEYS = [
    "ROUGELScore",
    "Sentences_Precision",
    "Sentences_Recall",
    "Words_Precision",
    "Words_Recall",
    "completeness",
    "hallucination",
    "irrelevance",
]

# Metrics where lower values indicate better performance
LOWER_IS_BETTER = {"hallucination", "irrelevance"}


def aggregate_metrics(data):
    """Compute mean and std for each metric from a list of eval entries."""
    buckets = {k: [] for k in METRICS_KEYS}
    for item in data:
        for k in METRICS_KEYS:
            val = item.get(k)
            if val is not None:
                buckets[k].append(val)
    avg = {k: mean(v) for k, v in buckets.items()}
    sd = {k: stdev(v) for k, v in buckets.items()}
    return avg, sd, len(data)


def get_breakdown(data, key_fn):
    """Group data by a key function and compute per-group metrics."""
    groups = {}
    for item in data:
        key = key_fn(item)
        groups.setdefault(key, []).append(item)
    return {
        k: dict(zip(["avg", "std", "count"], aggregate_metrics(items)))
        for k, items in groups.items()
    }


def discover_experiments(exp_dir):
    """Auto-discover eval_*.jsonl files and derive experiment names."""
    pattern = os.path.join(exp_dir, "eval_*.jsonl")
    experiments = []
    for path in sorted(glob.glob(pattern)):
        basename = os.path.basename(path)
        name = basename.replace("eval_", "").replace(".jsonl", "")
        output_file = basename.replace("eval_", "output_")
        experiments.append({
            "name": name,
            "eval_file": basename,
            "output_file": output_file,
        })
    return experiments


def build_report(exp_dir, experiments, results, all_data):
    """Build the Markdown report string."""
    valid_names = [e["name"] for e in experiments if e["name"] in results]
    run_id = os.path.basename(exp_dir.rstrip("/"))

    md = []
    md.append("# RAG Evaluation Report\n")
    md.append(f"**Run**: `{run_id}`  ")
    md.append(f"**Configurations**: {len(valid_names)}  ")
    md.append(f"**Generated by**: `rag-eval-report` skill\n")
    md.append("---\n")

    # ── Overall Averages ──
    md.append("## Overall Average Metrics\n")
    header = "| Metric | " + " | ".join(valid_names) + " |"
    divider = "|--------|" + "|".join(["-------:"] * len(valid_names)) + "|"
    md.append(header)
    md.append(divider)
    for m in METRICS_KEYS:
        row = f"| {m} |"
        for name in valid_names:
            row += f" {results[name]['avg'].get(m, 0.0):.4f} |"
        md.append(row)
    md.append("")
    md.append(
        "**Sample Count**: "
        + ", ".join(f"{n}: {results[n]['count']}" for n in valid_names)
        + "\n"
    )

    # ── Standard Deviation ──
    md.append("## Standard Deviation\n")
    md.append(header)
    md.append(divider)
    for m in METRICS_KEYS:
        row = f"| {m} |"
        for name in valid_names:
            row += f" {results[name]['std'].get(m, 0.0):.4f} |"
        md.append(row)
    md.append("")

    # ── Delta (only for exactly 2 configs) ──
    if len(valid_names) == 2:
        a, b = valid_names
        md.append(f"## Delta ({b} − {a})\n")
        md.append("| Metric | Delta | Better |")
        md.append("|--------|------:|--------|")
        for m in METRICS_KEYS:
            va = results[a]["avg"].get(m, 0.0)
            vb = results[b]["avg"].get(m, 0.0)
            delta = vb - va
            if m in LOWER_IS_BETTER:
                winner = b if delta < 0 else (a if delta > 0 else "Tie")
            else:
                winner = b if delta > 0 else (a if delta < 0 else "Tie")
            sign = "+" if delta > 0 else ""
            md.append(f"| {m} | {sign}{delta:.4f} | {winner} |")
        md.append("")

    # ── Breakdown by Query Type ──
    md.append("## Breakdown by Query Type\n")
    for name in valid_names:
        md.append(f"### {name}\n")
        breakdown = get_breakdown(
            all_data[name],
            lambda item: item.get("query", {}).get("query_type", "Unknown"),
        )
        for qtype in sorted(breakdown):
            info = breakdown[qtype]
            md.append(f"**{qtype}** (n={info['count']})\n")
            md.append("| Metric | Mean | Std |")
            md.append("|--------|-----:|----:|")
            for m in METRICS_KEYS:
                md.append(
                    f"| {m} | {info['avg'].get(m, 0.0):.4f} | {info['std'].get(m, 0.0):.4f} |"
                )
            md.append("")

    # ── Breakdown by Domain ──
    md.append("## Breakdown by Domain\n")
    for name in valid_names:
        md.append(f"### {name}\n")
        breakdown = get_breakdown(
            all_data[name], lambda item: item.get("domain", "Unknown")
        )
        for domain in sorted(breakdown):
            info = breakdown[domain]
            md.append(f"**{domain}** (n={info['count']})\n")
            md.append("| Metric | Mean | Std |")
            md.append("|--------|-----:|----:|")
            for m in METRICS_KEYS:
                md.append(
                    f"| {m} | {info['avg'].get(m, 0.0):.4f} | {info['std'].get(m, 0.0):.4f} |"
                )
            md.append("")

    # ── Experiment Details ──
    md.append("## Experiment Details\n")
    md.append("| Config | Eval File | Output File |")
    md.append("|--------|-----------|-------------|")
    for exp in experiments:
        if exp["name"] in valid_names:
            md.append(
                f"| {exp['name']} | `{exp['eval_file']}` | `{exp['output_file']}` |"
            )
    md.append("")

    return "\n".join(md)


def main():
    parser = argparse.ArgumentParser(
        description="Aggregate RAG evaluation JSONL files into a Markdown report."
    )
    parser.add_argument("experiment_dir", help="Directory containing eval_*.jsonl files")
    parser.add_argument(
        "--output_dir",
        default=None,
        help="Output directory for the report (default: <experiment_dir>/result/)",
    )
    args = parser.parse_args()

    exp_dir = os.path.abspath(args.experiment_dir)
    output_dir = args.output_dir or os.path.join(exp_dir, "result")
    os.makedirs(output_dir, exist_ok=True)

    # Discover experiments
    experiments = discover_experiments(exp_dir)
    if not experiments:
        print(f"Error: No eval_*.jsonl files found in {exp_dir}", file=sys.stderr)
        sys.exit(1)

    # Load and aggregate
    results = {}
    all_data = {}
    for exp in experiments:
        filepath = os.path.join(exp_dir, exp["eval_file"])
        data = load_jsonl(filepath)
        if data:
            avg, sd, count = aggregate_metrics(data)
            results[exp["name"]] = {"avg": avg, "std": sd, "count": count}
            all_data[exp["name"]] = data
            print(f"  ✓ {exp['eval_file']}: {count} entries")

    if not results:
        print("Error: No valid evaluation data found!", file=sys.stderr)
        sys.exit(1)

    # Generate report
    report = build_report(exp_dir, experiments, results, all_data)
    output_path = os.path.join(output_dir, "evaluation_report.md")
    with open(output_path, "w", encoding="utf-8") as f:
        f.write(report)

    print(f"\n✅ Report saved to: {output_path}")
    print(f"   {len(results)} configuration(s), {sum(r['count'] for r in results.values())} total entries")


if __name__ == "__main__":
    main()
